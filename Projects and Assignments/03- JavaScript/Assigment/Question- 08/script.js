// Question 08. Demonstrate the use of trim() to remove any trailing whitespace at the beginning and the end of a string.

// NOTE: The `trim()` method in JavaScript is used to remove whitespace from the beginning and end of a string. This includes spaces, tabs, and newlines.

let str = "   Hello, World!     ";
let newStr = str.trim();
console.log(newStr); // "Hello, World!"
