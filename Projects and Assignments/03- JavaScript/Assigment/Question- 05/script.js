// 05. Declare a varibale and assign string value to it and then split it into an array using split() method 

/* NOTE: The split() method in JavaScript is a built-in method of the String object that splits a string 
into an array of substrings based on a specified separator or delimiter. */

let str = "hello,world,how,are,you";
let strArray = str.split(",");

console.log(strArray);  // [ "hello", "world", "how", "are", "you" ]
